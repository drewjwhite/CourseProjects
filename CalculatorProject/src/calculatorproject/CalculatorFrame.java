    //Displays correct error message when called by another method
    public void errorMessages(int errorMarker) {
        if (errorMarker == 1) {
            pneMessages.showMessageDialog(this, "Division by zero is not "
                    + "allowed.", "Error Message",
                    javax.swing.JOptionPane.INFORMATION_MESSAGE);
            Calculations.reset();
            txtDisplay.setText("0.0");
        } else if (errorMarker == 2) {
            pneMessages.showMessageDialog(this, "ERROR: Please provide a "
                    + "positive integer between 0 and 20", "Value out "
                    + "of range",
                    javax.swing.JOptionPane.ERROR_MESSAGE);
            Calculations.reset();
            txtDisplay.setText("0.0");
        } else if (errorMarker == 3) {
            pneMessages.showMessageDialog(this, "Hey goober, Imma let you "
                    + "finish, but you can't put two decimal points in there!"
                    + " The first decimal point was the greatest decimal "
                    + "point of all time!", "What are you thinking?",
                    javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }

