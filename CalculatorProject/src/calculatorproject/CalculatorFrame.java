    //Changes current displa from x to 1/x
    private void btnFractionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFractionActionPerformed
        try {
            if (Double.parseDouble(txtDisplay.getText()) == 0) {
                throw new ArithmeticException();
            } else {
                double value = Double.parseDouble(txtDisplay.getText());
                value = 1 / value;
                txtDisplay.setText(Double.toString(value));
            }
        } catch (ArithmeticException ae) {
            errorMessages(1);
        }
    }//GEN-LAST:event_btnFractionActionPerformed

